    public void kd_auto(){
        try {
            Connection koneksi = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_inventory","root","");
            Statement stat = koneksi.createStatement();
            ResultSet rs = stat.executeQuery("SELECT MAX(kd_keluar) AS id FROM pengeluaran");
            while(rs.next()){
                Object[] obj = new Object[1];
                obj[0] = rs.getString("id");
                if(obj[0]==null){
                    obj[0]= "KLR000";
                }
                String str_kd = (String)obj[0];
                String kd = str_kd.substring(3,6);
                int int_code = Integer.parseInt(kd);
                int_code++;
                String a = String.format("%03d", int_code);
                String b = "KLR" + a;
                txtKd.setText(b);
            }
        } catch (Exception ex) {
            System.out.println(ex);
        }      
     }
    
