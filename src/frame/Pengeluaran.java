    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        cls_pengeluaran keluar = new cls_pengeluaran();
        keluar.kd_keluar = txtKd.getText();
        keluar.buah = txtBuah.getText();
        keluar.tgl_keluar = txtTanggal.getText();
        keluar.jumlah = txtJumlah.getText();
        keluar.kirim = txtKirim.getText();
        try{
            Linear barcode = new Linear();
            barcode.setType(Linear.CODE128B);
            barcode.setData(txtKd.getText());
            barcode.setI(11.0f);
            String fname = txtKd.getText();
            barcode.renderBarcode("src/Barcode_Pengeluaran/" + fname + ".png");
            keluar.submit();
            ViewData();
            reset();
            JOptionPane.showMessageDialog(null, "Data dan Barcode Berhasil disimpan");
        } catch (SQLException ex) {
            Logger.getLogger(Penyimpanan.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(Pengeluaran.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

