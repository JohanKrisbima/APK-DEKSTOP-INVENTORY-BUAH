    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try{
            String sql = "INSERT pengeluaran set kd_keluar = '"+txtKd.getText()+"' ,tgl_keluar='"+txtTanggal.getText()+"' ,buah='"+txtBuah.getText()+"' ,jumlah='"+txtJumlah.getText()+"' ,dikirim='"+txtKirim.getText()+"' On Duplicate Key Update kd_keluar ='"+txtKd.getText()+"' ,tgl_keluar='"+txtTanggal.getText()+"' ,buah='"+txtBuah.getText()+"' ,jumlah='"+txtJumlah.getText()+"' ,dikirim='"+txtKirim.getText()+"'"; 
            java.sql.Connection conn = (Connection)conek.GetConnection();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Edit data berhasil");
        }
        catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        ViewData();
        reset();
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        String ObjButtons[]= {"Ya","Tidak"};
        int PromptResult = JOptionPane.showOptionDialog(null, "hapus ?", "Message", JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null, ObjButtons, ObjButtons[1]);
        if(PromptResult==0){
            cls_pengeluaran klr = new cls_pengeluaran();
            klr.kd_keluar = txtKd.getText();
            try{
                klr.hapus();
                JOptionPane.showMessageDialog(null, "Berhasil dihapus");
                ViewData();
                reset();
            }
            catch (Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

